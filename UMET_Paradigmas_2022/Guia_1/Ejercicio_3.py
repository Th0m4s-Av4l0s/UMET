base = 0.0
altura = 0.0

# La base adquiere un valor por medio de un input
base = float(input("Valor de la base: "));

#El valor del cuadrado de la base es igual a la altura
altura = base**2

#Multiplico la base * la altura entre parentesis, y luego
# lo divido por 2
print(f"area = {(base * altura) / 2}")

