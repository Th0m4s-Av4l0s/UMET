# Metodo .islower() sirve para identificar
# si una cadena tiene todos sus caracteres en
# minusculas y devuelve un valor en forma bool
# EJ:

#En este caso devolver√° TRUE
cadena = "hola"
print(cadena.islower());

#Metodo lower() convierte cualquier cadena en
# minusculas
# EJ:

cadena = "HOLA"
print(cadena.lower());

#Metodo .isupper() funciona igual que islower()
# al identificar si una cadena tienen todos sus
# caracteres en mayusculas y devuelve un valor
# en forma de bool

#En este caso retornaria False
cadena = "hola"
print(cadena.isupper())


#Metodo .upper() lo mismo que lower, convierte
# todas los caracteres de una cadena en 
# mayusculas

cadena = "hola"
print(cadena.upper());
