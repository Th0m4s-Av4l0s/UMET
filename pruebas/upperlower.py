#Con.lower() convierto caracteres en minuscula
#Con .upper() convierto en mayuscula

saludo = "HoLA"
saludo = saludo.lower()

if(saludo == "hola"):
    print(saludo + "<- el lower funciona")

mayuscula = "hola"
mayuscula = mayuscula.upper()

if(mayuscula == "HOLA"):
    print(mayuscula + "<- el upper funciona")